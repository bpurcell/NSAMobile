    <div data-role="content">
        <h3>Basic Practice</h3>
        <p>Nailing: Set the nail in the countersink in the top surface of the slate.  Do not over-nail or under-nail.  Slate shingles must hang on their nails.</p>
        <p>Nails: 11-gauge, smooth shank, copper wire slating nails are used most frequently.</p>
        <p>Number of Nails: Use 2 nails per slate.</p>
        <p>Nail Length:  Generally twice the thickness of the slate plus 1”.</p>

        <h3>Special Considerations</h3>
        <p>Gypsum and Concrete Roof Decks:  Use smooth shank nails, not ring shank.  Stainless steel nails may be needed to avoid bending.</p>
        <p>Gauge: Use 10-gauge nails for larger slates, hardwood roof decks, and where longer nails are required (e.g., hips).</p>
        <p>4 Nails: Use 4 nails per slate for slates measuring ≥ 3/4” in thickness and ≥ 20” in length.  Place the 2 additional nails 2” above the regular nail holes.</p>
        <p>Flashings: Do not nail slates through flashings.  Move the slating nails up, place one nail above the other on one side of the slate, or use copper wires secured to the roof deck upslope of the flashing.</p>
