        <h1>Headwall Flashing</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice </h3>
        <p>Purpose: Provide a watertight interface where the top edge of a sloped roof 
          meets a wall. Intercept water running down face of the wall as well as water 
          that penetrates behind the wall cladding. </p>
        <p>Laps: Adjacent lengths of apron flashing are typically overlapped 4”-6” without 
          rivets or solder. </p>
        <p>Headlap: Lap the apron over the slates sufficiently to maintain the required 
          headlap. </p>
        <p>Protection From Wind-Driven Rain: The vertical leg of the apron should be lapped 
          by the wall underlayment 2” minimum. </p>
        <h3>Special Considerations </h3>
        <p>Exposure: As the field courses approach the headwall, the exposure of each 
          course of slate can be reduced slightly to result in a straight appearance at 
          the wall and the finishing course having approximately the same exposure as 
          the field of the roof. </p>
        <p>Finishing Course: The finishing course can be set vertically or horizontally. 
        </p>
        <p>Counterflashing: If there is no metal counterflashing in the wall, the wall 
          cladding can lap over the vertical leg of the apron to serve as the counterflashing. 
          Siding should terminate approximately _” above the roof surface. </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 123-130. </p>

