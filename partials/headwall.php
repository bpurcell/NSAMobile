        <h1>Apron Corner</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice </h3>
        <p>Purpose: Provide a watertight transition where the apron turns a corner; typical 
          at dormers, skylights, chimneys, etc. </p>
        <p>Joints: Flashing corners should be soldered watertight. </p>
        <h3>Special Considerations </h3>
        <p>Underlayment: Underlayment (building wrap) at wall must lap over the top of 
          the apron. </p>
        <p>Variations: there are other acceptable techniques of forming a corner, but 
          overlap and soldering must be achieved as shown. </p>
        <p>For additional information see<em> Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 123-130. </p>

