        <h1>Underlayment Detail</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice </h3>
        <p>Purpose: Underlayments help shed water beyond the roof eave edge. Underlayments 
          are required by building code. </p>
        <p>Material: Asphalt saturated felts, polymer modified bitumen sheets, and synthetic 
          sheets are used. Installation: The underlayment should shed water on top of 
          the eave drip edge metal, gutter apron or other eave flashing. </p>
        <h3>Special Considerations </h3>
        <p>Quality: Quality underlayments should be used with lower roof slopes and wetter 
          roof areas. A long term service life for the underlayment is needed for a reliable 
          weather-tight roof system. </p>
        <p>Material: See Underlayments, page 7 of this Field Guide. </p>
        <p>Various Methods: Single ply, multiple ply, and adhered membranes are used. 
        </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 70-75. </p>

