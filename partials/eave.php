        <h1>Eave Cant Detail</h1>
    </div><!-- /header -->
    <div data-role="content">


        <h3>Basic Practice </h3>
        <p>Purpose: The butt end of each slate is raised by the course below to create 
          the field angle of the slates. Therefore an eave cant is needed under the starter 
          course slate to raise the butt end to the same angle as the other courses. </p>
        <p>Material: The cant is wood lath, a beveled wood strip, formed metal or other 
          method of raising the starter course. </p>
        <p>Location: The cant is best located under the eave flashing, drip edge or gutter 
          apron Thickness: The cant thickness is similar to the thickness of the first 
          course of slate. </p>
        <h3>Special Considerations </h3>
        <p>Purpose: Each course should conform to the field angle of the course below 
          to give support and strength to the slate and minimize infiltration of wind-blown 
          rain and snow. </p>
        <p>Drainage: Allow for incidental drainage of water from behind the cant. </p>
        <p>Location: If the cant is placed on top of the underlayment, 4' lengths with 
          gaps between will allow for drainage. </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 70-75. </p>


    </div>
</div>

