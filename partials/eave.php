        <h1>Metal Drip Detail</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice </h3>
        <p>Purpose: The drip edge helps guide water shedding off the edge of the roof. 
          It protects and creates a neat edge for roof deck or fascia. </p>
        <p>Material: Corrosion resistant metal is used. </p>
        <p>Installation: The metal is usually side lapped 3” and fastened at the top edge 
          every 12” to 24”. The underlayments or ice dam protection membrane should shed 
          water on top of the drip edge. </p>
        <h3>Special Considerations </h3>
        <p>Required: A drip edge may be a building code requirement, and is needed where 
          the roof deck edge is plywood or end grain lumber and where the deck edge is 
          irregular or gapped. </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 70-75. </p>



