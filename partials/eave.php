        <h1>Starter Course Detail</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice </h3>
        <p>Beginning: Installation of the slate shingles begins with laying the starter 
          course on top of cant, drip edge and underlayment. The starter course is then 
          covered with the first course, resulting in 2 layers of slate shingles at the 
          eave edge. </p>
        <p>Size: The minimum vertical dimension of the starter course is the exposed length 
          of the first course plus the appropriate headlap of 3” or 4”. The thickness 
          is the same thickness as the first course. </p>
        <p>Installation: The side joints of the starter slate offset the side joints of 
          the first course by 3”. Nail holes are located about 1_” from the top edge. 
        </p>
        <h3>Special Considerations </h3>
        <p>Purpose: The starter slate sheds water from the side joints of the first course. 
          The starter course along with the cant creates the field angle of the slate 
          shingles. </p>
        <p>Installation: Starter slates are laid with the beveled edge up or down depending 
          on local practice. </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 70-75. </p>



