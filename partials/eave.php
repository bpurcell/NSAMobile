        <h1>Ice Dam Detail</h1>
    </div><!-- /header -->
    <div data-role="content">


        <h3>Basic Practice </h3>
        <p>Required: Ice dam protection membranes are used at the eave as required by 
          building code, and anywhere ice and snow dams can occur. They are usually self-adhering 
          modified bitumen membranes. </p>
        <p>Installation: Ice dam protection membranes are installed according to the manufacturers' 
          instructions. Some are adhered directly to the roof and onto the fascia to be 
          covered by the drip edge. Some are adhered to the top of the drip edge or gutter 
          apron. Some are then covered with the loose laid field underlayment. </p>
        <h3>Special Considerations </h3>
        <p>Location: Ice dam protection membranes are usually installed to at least 24” 
          to 36” inside of the heated wall line. </p>
        <p>Stripping In: A strip about 12” wide may be used to strip in the metal drip 
          edge where the drip edge is laid on top of the ice dam protection membrane. 
        </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 70-75. </p>


