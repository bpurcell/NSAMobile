        <h1>Slate Hook</h1>
    </div><!-- /header -->
    <div data-role="content">
        <h3>Basic Practice</h3>
        <p>Replacement Slates: When replacing broken slates, the replacement slate should 
          match the existing in type, color, weathering characteristics, size, shape, 
          and texture. </p>
        <p>Repair Steps:</p>
        <ol>
          <li>Remove broken slate using slate ripper.</li>
          <li>Install slate hook.</li>
          <li>Slide new slate into position until it clears the hook of the slate hook, 
            then lower its butt end to the roof plane and pull the slate downslope until 
            it seats in the hook. </li>
        </ol>
        <p>Materials: Copper and stainless steel slate hooks are most commonly used. </p>
        <p>Size: Standard slate hooks are 3” long and are made to accommodate 1/4-inch-thick 
          slate laid with a 2” or 3” headlap. </p>
        <h3>Special Considerations</h3>
        <p>Replacement Slates: These can be new slate, reclaimed slate, or slates taken 
          from an attic stock left over from the original construction. </p>
        <p>Details:</p>
        <ol>
          <li>The ripper is used to remove the slate’s nails.</li>
          <li>A slightly thinner repair slate will make insertion easier.</li>
          <li>Slate hook is driven into the roof deck through the joint between the slates 
            in the course below with its butt end aligned with where the butt end of the 
            replacement slate needs to be. </li>
        </ol>
        <p>Materials: Galvanized steel slate hooks tend to rust and stain the roof, but 
          may be appropriate where only a few years of service life are required. </p>
        <p>Custom Size: Custom made slate hooks can be used to accommodate thicker slates, 
          but it is often more practical to use the nail and bib method instead. </p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 213-221.</p>
