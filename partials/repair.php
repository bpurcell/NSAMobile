        <h1>Nail and Bib</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice</h3>
        <p>Replacement Slates: When replacing broken slates, the replacement slate should 
        match the existing in type, color, weathering characteristics, size, shape, and 
        texture.</p>
        <p>Repair Steps:</p>
        <ol>
          <li>Remove broken slate using slate ripper.</li>
          <li>Slide new slate into position, aligning butt end with adjacent slates. </li>
          <li>Punch/drill a nail hole in the face of the replacement slate, thru the joint 
            in the course above and 1&#8221; below the butt of the slate two courses above. 
          </li>
          <li>Insert and set nail. </li>
          <li>Insert metal bib. </li>
        </ol>
        <p>Materials: Copper nails and 16 oz. copper bibs are most commonly used.</p>
        <h3>Special Considerations</h3>
        <p>Replacement Slates: These can be new slate, reclaimed slate, or slates taken 
          from an attic stock left over from the original construction.</p>
        <p>Details:</p>
        <ol>
          <li>The ripper is used to remove the slate&#8217;s nails.</li>
          <li>A slightly thinner repair slate will make insertion easier. </li>
          <li>The nail hole is placed in the bond line between the slates in the course 
            immediately above.</li>
          <li>Use 2 nails for larger slates.</li>
          <li>The bib typically measures 3&#8221; x 8&#8221; and is bent into a concave 
            shape to help prevent it from sliding out.</li>
        </ol>
        <p>Materials: The objective is to use materials with service lives equal to or 
          greater than that of the remaining service life of the slate itself. Thus, on 
          an older roof galvanized nails and sheet metal may be acceptable.</p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 213-221.</p>

