        <h1>Offset</h1>
    </div><!-- /header -->
    <div data-role="content">

        <h3>Basic Practice</h3>	
        <p>Offset: The distance between vertical joints in adjacent courses should be minimum 3”.  This will prevent leaks due to lateral movement of water.</p>
        <h3>Special Considerations</h3>	
        <p>Maintaining Offset: When laying single width slates, offset is automatically achieved by setting the edge of each slate at the center of the course below; the alternating vertical joints will align up the roof slope. When installing random width slates, offset is maintained as each slate is selected for installation.</p>
        <p>For additional information see <em>Slate Roofs: Design and Installation Manual</em>, 
          2010 Edition, pages 40, 42, 44 and 83.</p>

